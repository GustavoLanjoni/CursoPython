# Tipo de dados Boolean 
# Ao questionar algo em um programa
# so existem duas resposta possiveis 
# Sim (True) e falso (False)
# Exstem vários operadores para "questionar"
# Dentre eles o == que é um operador logico que 
# questiona se um valor e igual ao outro

print(2 == 21)
print(1 == 1)

print(type(10 == 10))
print(type(11 == 11))