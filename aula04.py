# Tios int e float
# int -> Número inteiro
# O tipo int representa qualquer numero 
# positivo ou negativo. int sem sinal é considerado
# positivo

print(11)
print(-10)

# Float -> Numero com ponta flutuante
# O tipo float representa qualquer número
# positivo ou negativo com ponto futuante
#float sem sinal e considerado positivo

print(1.200)
print(0.0)

# A função de type mostra o tipo que  python inferiu ao valor 
print(type(10), type("Gustavo"), type(1.20))